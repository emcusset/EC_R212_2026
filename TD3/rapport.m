Rapport — TD3
Points clés
1. Modèle événement → état → rendu
Le modèle événement → état → rendu permet de comprendre comment une interaction fonctionne : une action utilisateur déclenche un événement, cela modifie un état, puis l’affichage change. Par exemple, cliquer sur un bouton "like" augmente le nombre de likes et met à jour le texte à l’écran.
2. Rôle de classList.toggle
La méthode classList.toggle permet d’ajouter ou retirer une classe automatiquement. Elle fait le lien entre JavaScript et CSS : le JavaScript décide quand changer la classe, et le CSS définit l’apparence associée.
3. Manipulation du DOM
Le JavaScript permet de manipuler le DOM, c’est-à-dire modifier la page web en temps réel. Avec des outils comme querySelector, addEventListener et textContent, on peut sélectionner des éléments, réagir aux actions et changer leur contenu.
